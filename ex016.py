import math
print('======== EXERCICIO 016 ========')
num = int(input('Informe o numero que deseja saber a raiz quadrada: '))
raiz = math.sqrt(num)
print('A raiz quadrada de {} e {:.3f} e seu numero inteiro e {}' .format(num, raiz, math.floor(raiz)))
