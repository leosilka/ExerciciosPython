print('======== EXERCICIO 004 ========')
algo = input('Digite algo que deseja saber: ')
print('O tipo primitivo deste valor e', type(algo))
print('So tem espacos? ', algo.isspace())
print('Ele e um numero? ', algo.isnumeric())
print('Ele e alfanumerico? ', algo.isalnum())
print('E alfabetico? ', algo.isalpha())
print('Esta em maiusculas? ', algo.isupper())
print('Esta em mionusculas? ', algo.islower())
print('Esta capitalizada? ', algo.istitle())
